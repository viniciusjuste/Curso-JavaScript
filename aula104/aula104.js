let nome = new String('Bruno Pinho Campos')

// O método startsWith() retorna true se a string 'nome' começa com 'Pinho', caso contrário, retorna false.
console.log(nome.startsWith('Bruno'))

// O método endsWith() retorna true se a string 'nome' termina com 'Pinho', caso contrário, retorna false.
console.log(nome.endsWith('Pinho'))

// O método includes() retorna true se a string 'nome' contém 'Pinho', caso contrário, retorna false.
console.log(nome.includes('Pinho'))

// O método repeat(2) retorna uma nova string contendo a repetição da string 'nome' duas vezes.
console.log(nome.repeat(2))

// O método String.fromCodePoint(66) retorna o caractere correspondente ao código Unicode especificado, que neste caso é 'B'.
console.log(String.fromCodePoint(66))


